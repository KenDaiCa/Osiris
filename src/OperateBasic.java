

public class OperateBasic {
	
	public static void main(String[] args) {
		int a = 10;
		int b = 5;

		System.out.println("---------------");
		
		System.out.println("Chia lay phan du 10/3: " + a%b);
		System.out.println("Chia lay phan du 3/10: " + b%a);
		
		System.out.println("---------------");
		a+=a; //20
		System.out.println(a);
		
		System.out.println("---------------");
		a/=b;
		System.out.println(a);
		
		System.out.println("---------------");
		a-=a; //0
		System.out.println(a);
		
		System.out.println("---------------");
		b*=b;
		System.out.println(b);
		
		System.out.println("---------------");
		

	
		
		
		
		
	}
	
}
